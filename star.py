import turtle
turtle.Screen().bgcolor("yellow")
turtle.setup(500,500)
t=turtle.Turtle()
t.color("red")
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.forward(100)  
t.penup()
t.right(150)
t.forward(50)
t.right(90)
t.pendown()
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
turtle.done()